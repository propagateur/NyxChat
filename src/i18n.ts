const langListeners = new Set<(lang: Lang) => void>();

export function getLanguage(): Lang {
  const saved = localStorage.getItem("nyx.lang");
  if (saved && saved in dictionaries) return saved as Lang;
  const prefix = navigator.language.toLowerCase().slice(0, 2);
  if (prefix in dictionaries) return prefix as Lang;
  return "en";
}

export function setLanguage(lang: Lang) {
  localStorage.setItem("nyx.lang", lang);
  langListeners.forEach((listener) => listener(lang));
}

export function useTranslation() {
  const [lang, setLang] = useState<Lang>(getLanguage);

  useEffect(() => {
    langListeners.add(setLang);
    return () => {
      langListeners.delete(setLang);
    };
  }, []);

  function t(key: string) {
    return dictionaries[lang][key] ?? dictionaries.en[key] ?? key;
  }

  return { lang, setLanguage, t };
}
